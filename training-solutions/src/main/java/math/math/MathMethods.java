package math.math;

public class MathMethods {

	public static void main(String[] args) {
		System.out.println(Math.max(211, 167));
		System.out.println(Math.min(211, 167));
		System.out.println(Math.PI);
		System.out.println(Math.round(Math.PI));
		System.out.println(Math.round(21.4481));
		System.out.println(Math.abs(-21.4481));
		System.out.println(Math.negateExact(214));
		System.out.println(Math.addExact(1684, -44));
		System.out.println(Math.subtractExact(1684, -44));
		System.out.println(Math.pow(18.87, -4.44));
		System.out.println(Math.incrementExact(4));
		System.out.println(Math.decrementExact(4));
		System.out.println(Math.random());
	}
}
